public class TimeUtils {
    /**
     * 以毫秒为基数
     */
    public static final int MSEC_MINUTE = 60 * 1000;
    /**
     * 以毫秒为基数
     */
    public static final int MSEC_HALF_HOUR = 30 * MSEC_MINUTE;
    /**
     * 以毫秒为基数
     */
    public static final int MSEC_HOUR = 60 * MSEC_MINUTE;
    /**
     * 以毫秒为基数
     */
    public static final int MSEC_DAY = 24 * MSEC_HOUR;
    /**
     * 以毫秒为基数
     */
    public static final int MSEC_MONTH = 30 * MSEC_DAY;
    /**
     * 以毫秒为基数
     */
    public static final int MSEC_YEAR = 365 * MSEC_DAY;

    // ==================================================
    /**
     * 以秒为基数
     */
    public static final int SEC_MINUTE = 60;
    /**
     * 以秒为基数
     */
    public static final int SEC_HALF_HOUR = 30 * SEC_MINUTE;
    /**
     * 以秒为基数
     */
    public static final int SEC_HOUR = 60 * SEC_MINUTE;
    /**
     * 以秒为基数
     */
    public static final int SEC_DAY = 24 * SEC_HOUR;
    /**
     * 以秒为基数
     */
    public static final int SEC_MONTH = 30 * SEC_DAY;
    /**
     * 以秒为基数
     */
    public static final int SEC_YEAR = 365 * SEC_DAY;
}
